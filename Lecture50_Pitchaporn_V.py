def addNumber(x, y):
  print(x, '+', y, '=', x+y)

def subtractNumber(x, y):
  print(x, '-', y, '=', x-y)

def multiplyNumber(x, y):
  print(x, 'x', y, '=', x*y)

def divideNumber(x, y):
  print(x, '/', y, '=', x/y)

addNumber(100,25)
subtractNumber(100,25)
multiplyNumber(100,25)
divideNumber(100,25)